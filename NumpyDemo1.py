import numpy as np

# Creating a 1D array
array_1d = np.array([1, 2, 3, 4, 5])
print("1D array:", array_1d)

# Adding a scalar to each element
array_1d_add = array_1d + 10
print("1D array after adding 10:", array_1d_add)

# Multiplying each element by a scalar
array_1d_mul = array_1d * 2
print("1D array after multiplying by 2:", array_1d_mul)


# Creating a 2D array
array_2d = np.array([[1, 2, 3], [4, 5, 6]])
print("2D array:\n", array_2d)

# Adding a scalar to each element
array_2d_add = array_2d + 10
print("2D array after adding 10:\n", array_2d_add)

# Element-wise multiplication
array_2d_mul = array_2d * 2
print("2D array after multiplying by 2:\n", array_2d_mul)

# Creating a 3D array
array_3d = np.array([[[1, 2], [3, 4]], [[5, 6], [7, 8]]])
print("3D array:\n", array_3d)

# Adding a scalar to each element
array_3d_add = array_3d + 10
print("3D array after adding 10:\n", array_3d_add)

# Element-wise multiplication
array_3d_mul = array_3d * 2
print("3D array after multiplying by 2:\n", array_3d_mul)


